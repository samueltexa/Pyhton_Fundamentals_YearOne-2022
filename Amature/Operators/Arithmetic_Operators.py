# Arithmetic operators are used with numeric values to perform common mathematical operations:

# 1.Addition(+). adds the two operands and returns the sum.
# Example
num1 = 2
num2 = 4
print(num1 + num2)

# 2.Multiplication(*). multiplies two operands and returns the product.
# Example
num1 = 2
num2 = 4
print(num1 * num2)

# 3.Subruction(-). subtracts the two operands and returns the difference.
# Example
num1 = 2
num2 = 4
print(num1 - num2)

# 4.Division(/)
# Example
num1 = 2
num2 = 4
print(num1 / num2)

# 5.Modulus(%). returns the remainder when the first operand is divided by the second
# Example
num1 = 5
num2 = 2
print(num1 % num2)

# 6.Exponential(**). Returns first raised to power second
# Example
num1 = 2
num2 = 4
print(num1 ** num2)

# 7.Floor Division(//). returns the exact whole number without the reminder
# Example
num1 = 13
num2 = 4
print(num1 // num2)
